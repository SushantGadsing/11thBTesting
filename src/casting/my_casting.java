package casting;

public class my_casting {

	public static void main(String[] args) {
		//implicit casting Lower to Higher
		
		int a=10;
		
		System.out.println("this is value of a "+a );

		double b=a;
				System.out.println("This is value of b "+b);
				
				
		// Explicit casting Lower to higher
				
				double c=10.1233;
				System.out.println("This is value of c "+c);
				
				int d=(int) c; 
				System.out.println("this is value of d "+d);
				
		
	}

}
